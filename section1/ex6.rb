# assigns a types_of_people variable
types_of_people = 10
# interpolates types_of_people into a sentence
x = "There are #{types_of_people} types of people."
# assigns a binary variable
binary = "binary"
# assigns a do_not variable
do_not = "don't"
# interpolates binary and do_not into a sentence
y = "Those who know #{binary} and those who #{do_not}."

# display the results of evaluating x
puts x
# display the results of evaluating y
puts y 

# prints an interpolated sentence using the x variable
puts "I said: #{x}."
# prints an interpolated sentence using the y variable
puts "I also said: '#{y}'."

# assigns a hilarious variable
# hilarious = false
hilarious = true
# assigns a joke_evaluation variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# display the results of evaluating joke_evaluation
puts joke_evaluation

# assings a w variable
w = "This is the left side of ..."
# assigns a e variable
e = "a string with a right side."

# display the results of w plus e by concatonating it
puts w + e 

